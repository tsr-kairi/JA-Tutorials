// Strings Method
const myVariable = "Hello World";

// The length property
console.log(myVariable.length);

console.log(myVariable.charAt(0));

console.log(myVariable.indexOf("hello"));

console.log(myVariable.lastIndexOf("llo"));

console.log(myVariable.slice(5, 10));

console.log(myVariable.substring(5, 10));

console.log(myVariable.substr(5, 10));

console.log(myVariable.toUpperCase());

console.log(myVariable.toLowerCase());

console.log(myVariable.includes("llo"));

console.log(myVariable.replace("llo", "world"));
console.log(myVariable.split("llo"));
